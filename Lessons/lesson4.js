// Relational or compatison operators 

// > - more than 
// < - less than 
// >= - more than equal 
// <= - less than equal 

// Always returns as a boolean 

var result = 6 <= 5
console.log(result)

// eqaulity operators    

var x = 1 
console.log(x == '1') // lose comparison compares the variable to the left to the right 
console.log(x === '1') // strict comparison checks the value and the data type 1 is a number but in the console it's a string
console.log(x === 1) // correct strict comparison data and value types are the same 